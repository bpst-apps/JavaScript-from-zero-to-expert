// switch statement

const weekDay = 'friday';

switch (weekDay) {
    case 'monday':
        console.log('This is monday today!!!');
        break;
    case 'tuesday':
        console.log('This is tuesday today!!!');
        break;
    case 'wednesday':
        console.log('This is wednesday today!!!');
        break;
    case 'thursday':
        console.log('This is thursday today!!!');
        break;
    case 'friday':
        console.log('This is friday today!!!');
        break;
    case 'saturday':
        console.log('This is saturday today!!!');
        break;
    case 'sunday':
        console.log('This is sunday today!!!');
        break;
    default:
        console.log('Not a valid day!');
} 